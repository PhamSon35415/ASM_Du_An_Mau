public class user {
    private String tk;
    private String mk;
    private int chucvu;

    public user(String tk, String mk, int chucvu) {
        this.tk = tk;
        this.mk = mk;
        this.chucvu = chucvu;
    }

    public user() {
    }

    public String getTk() {
        return tk;
    }

    public void setTk(String tk) {
        this.tk = tk;
    }

    public String getMk() {
        return mk;
    }

    public void setMk(String mk) {
        this.mk = mk;
    }

    public int getChucvu() {
        return chucvu;
    }

    public void setChucvu(int chucvu) {
        this.chucvu = chucvu;
    }
    
}
